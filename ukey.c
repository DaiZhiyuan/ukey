/* A mutex will ensure that only one process accesses our device */
static DEFINE_MUTEX(mmap_device_mutex);

struct mmap_info {
	char *data;
	int reference;
};

/* Device variables */
static dev_t ukey_dev_t = 0;
static struct cdev *ukey_cdev;
static struct class *ukey_class;

void mmap_open(struct vm_area_struct *vma)
{
	struct mmap_info *info = (struct mmap_info *)vma->vm_private_data;
	info->reference++;
}

void mmap_close(struct vm_area_struct *vma)
{
	struct mmap_info *info = (struct mmap_info *)vma->vm_private_data;
	info->reference--;
}

static int mmap_fault(struct vm_area_struct *vma, struct vm_fault *vmf)
{
	struct page *page;
	struct mmap_info *info;

    unsigned long address = (unsigned long)vmf->virtual_address;
    if (address > vma->vm_end) {
        printk("invalid address\n");
        return VM_FAULT_SIGBUS;
    }


	info = (struct mmap_info *)vma->vm_private_data;
	if (!info->data) {
		printk("No data\n");
		return VM_FAULT_SIGBUS;
	}
    // 通过一个已知的虚拟地址来获取其对应的页框page
	page = virt_to_page(info->data);

    // refcount++
	get_page(page);

	vmf->page = page;

	return 0;
}

struct vm_operations_struct mmap_vm_ops = {
	.open = mmap_open,
	.close = mmap_close,
	.fault = mmap_fault,
};

int op_mmap(struct file *filp, struct vm_area_struct *vma)
{
	vma->vm_ops = &mmap_vm_ops;
	vma->vm_flags |= VM_RESERVED;
    // assign the file private data to the vm private data
	vma->vm_private_data = filp->private_data;
	mmap_open(vma);
	return 0;
}

int mmapfop_close(struct inode *inode, struct file *filp)
{
	struct mmap_info *info = filp->private_data;

	free_page((unsigned long)info->data);
	kfree(info);
	filp->private_data = NULL;

	mutex_unlock(&mmap_device_mutex);

	return 0;
}

int mmapfop_open(struct inode *inode, struct file *filp)
{
	struct mmap_info *info = NULL;

	if (!mutex_trylock(&mmap_device_mutex)) {
		printk(KERN_WARNING
		       "Another process is accessing the device\n");
		return -EBUSY;
	}

	info = kmalloc(sizeof(struct mmap_info), GFP_KERNEL);
	info->data = (char *)get_zeroed_page(GFP_KERNEL);
    memcpy(info->data, ukey, 4096);

	/* assign this info struct to the file */
	filp->private_data = info;
	return 0;
}

